package main

import (
	"fmt"
)

func main() {
	//declare and assign
	x := 42
	fmt.Println(x)
	//just assign
	x = 99
	fmt.Println(x)

	//statement - declare and assign value of expression
	// + - operator
	// 100,24 - operands
	y := 100 + 24
	fmt.Println(y)

	//each line is a statement, statements are usually made up of expressions
	//100 + 24 is an expression
}
